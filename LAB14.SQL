ALTER FUNCTION PRINTNUM()
RETURNS VARCHAR(100)
AS
BEGIN
	DECLARE @I INT=1,@DEMO VARCHAR(100)=''
	WHILE @I<=10
	BEGIN
		SET @DEMO=@DEMO+CAST(@I AS VARCHAR(100))+','
		SET @I=@I+1
	END
	RETURN @DEMO
END

SELECT DBO.PRINTNUM()

--2
CREATE FUNCTION CHECK_ODD_EVEN(@NUM INT)
RETURNS VARCHAR(100)
AS
BEGIN
	DECLARE @DEMO VARCHAR(100)
	IF @NUM%2=0
	BEGIN
		SET @DEMO='EVEN'
	END
	ELSE
	BEGIN
		SET @DEMO='ODD'
	END
	RETURN @DEMO
END

SELECT DBO.CHECK_ODD_EVEN(5)

