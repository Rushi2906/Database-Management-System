ALTER FUNCTION SUMNUM()
RETURNS INT
AS
BEGIN
	DECLARE @I INT,@SUM INT
	SET @I=1
	SET @SUM=0
	WHILE @I<=50
	BEGIN
		SET @SUM=@SUM+@I
		SET @I=@I+1
	END
	RETURN @SUM
END

SELECT DBO.SUMNUM()

