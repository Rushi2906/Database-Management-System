ALTER FUNCTION SUMEVEN()
RETURNS INT
AS
BEGIN
	DECLARE @I INT,@SUM INT
	SET @I=1
	SET @SUM=0
	WHILE @I<=20
	BEGIN
		IF @I%2=0
		SET @SUM=@SUM+@I
		SET @I=@I+1
	END
	RETURN @SUM
END

SELECT DBO.SUMEVEN()

