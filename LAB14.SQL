ALTER FUNCTION PRINTODDNUM()
RETURNS VARCHAR(100)
AS
BEGIN
	DECLARE @I INT,@DEMO VARCHAR(100)=''
	SET @I=1
	WHILE @I<=10
	BEGIN
		IF @I%2!=0
			SET @DEMO=@DEMO+CAST(@I AS VARCHAR(100))+','
			SET @I=@I+1
	END
	RETURN @DEMO
END

SELECT DBO.PRINTODDNUM()

