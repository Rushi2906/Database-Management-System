ALTER FUNCTION CHECKPRIME(@NUM INT)
RETURNS VARCHAR(100)
AS
BEGIN
	DECLARE @I INT,@RETVAL VARCHAR(10)
	SET @I=2
	SET @RETVAL='PRIME'
	WHILE(@I<=@NUM)
	BEGIN
		IF @NUM%@I!=0
		BEGIN
			BREAK
		END
		ELSE
		BEGIN
			SET @RETVAL='NOTPRIME'
			BREAK
		END
		SET @I=@I+1
	END
	RETURN @RETVAL
END

SELECT DBO.CHECKPRIME(10)


